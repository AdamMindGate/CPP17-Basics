#include <iostream>

using namespace std;

/*Problem #1: Math operations*/
int main()
{
    int a, b;
    cin>>a>>b;
    cout <<a<<" + "<<b<<" = "<<a + b<< endl;
    cout <<a<<" - "<<b<<" = "<<a - b<< endl;
    cout <<a<<" * "<<b<<" = "<<a * b<< endl;
    cout <<a<<" / "<<b<<" = "<<a / b<< endl;

    return 0;
}

